
# 20.WAP to find grade

x=str(input('enter the grade : '))

if (x=='e' or x=='E'):
    print('Excellent')

elif (x=='v' or x=='V'):
    print('Very good')

elif (x=='g' or x=='G'):
    print('Good')

elif (x=='a' or x=='A'):
    print('Average')

elif (x=='f' or x=='F'):
    print('Fail')

else:
    print('IN VALID')
