## WAP to read any day number in integer and display day name in the word
## Input:  4               expected : Thusday


weekday=4
if weekday==0:
    print("\nsunday")
elif weekday==1:
    print("\nmonday")
elif weekday==2:
    print("\ntuesday")
elif weekday==3:
    print("\nwednesday")
elif weekday==4:
    print("\nthusday")
elif weekday==5:
    print("\nfriday")
elif weekday==6:
    print("\nsatarday")
else:
    print("\n please enter week number: ")
    
